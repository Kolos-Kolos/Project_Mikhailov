#Три цветовода выращивают розы. Определить, какие из известных сортов роз «Анжелика»,
#«Виктория», «Гагарин», «Катарина», «Юбилейная», «Южная» имеются у каждого
#цветоводы, есть хотя бы у одного из цветоводов и каких нет ни у одного из цветоводов.


try:
    # Все известные сорта роз
    all_roses = {
        "Анжелика",
        "Виктория",
        "Гагарин",
        "Катарина",
        "Юбилейная",
        "Южная"
    }

    # Розы первого цветовода
    gardener_1 = {
        "Анжелика",
        "Виктория",
        "Гагарин"
    }

    # Розы второго цветовода
    gardener_2 = {
        "Виктория",
        "Гагарин",
        "Катарина"
    }

    # Розы третьего цветовода
    gardener_3 = {
        "Виктория",
        "Гагарин",
        "Юбилейная"
    }

    # Есть у каждого цветовода
    common_roses = gardener_1 & gardener_2 & gardener_3

    # Есть хотя бы у одного цветовода
    all_grown_roses = gardener_1 | gardener_2 | gardener_3

    # Нет ни у одного цветовода
    absent_roses = all_roses - all_grown_roses

    print("Сорта роз, имеющиеся у каждого цветовода:")
    print(common_roses)

    print("\nСорта роз, имеющиеся хотя бы у одного цветовода:")
    print(all_grown_roses)

    print("\nСорта роз, которых нет ни у одного цветовода:")
    print(absent_roses)

except Exception as error:
    print("Произошла ошибка:", error)
